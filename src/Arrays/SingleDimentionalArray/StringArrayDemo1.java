package Arrays.SingleDimentionalArray;

public class StringArrayDemo1 {

    public static void main(String[] args) {

        String[] a = new String[5];

        a[0] = "amol";
        a[1] = "vijay";
        a[2] = "surya";
        a[3] = "Manshi";
        a[4] = "Priyanka";

        for(int i=0 ; i<5 ; i++)
          System.out.println(a[i]);
    }
}
