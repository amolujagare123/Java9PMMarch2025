package Arrays.SingleDimentionalArray;

public class CharArrayDemo1 {

    public static void main(String[] args) {

        char[] a = new char[5];

        a[0] = 'a';
        a[1] = 'D';
        a[2] = '1';
        a[3] = '@';
        a[4] = '#';

        for(int i=0 ; i<5 ; i++)
          System.out.println(a[i]);
    }
}
